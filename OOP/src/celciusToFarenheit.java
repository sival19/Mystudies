public class celciusToFarenheit {
    public static void main(String[] args) {
        //set celcius and calclulate it to fahrenheit
        int celcius = 10;
        int farenheit = (celcius * 9 / 5) + 32;
        //print out
        System.out.println(celcius + " celcius is: " + farenheit + " Farenheit");
    }
}
